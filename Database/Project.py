class Project:

    def __init__(self, p_id, name, owner, space, size, contributor_count, contributors, dates, issues):
        self.p_id = p_id
        self.name = name
        self.owner = owner
        self.space = space
        self.size = size
        self.contributor_count = contributor_count
        self.contributors = contributors
        self.dates = dates
        self.issues = issues
